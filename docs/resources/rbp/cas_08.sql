 * 1. Napraviti tabelu TEST sa kolonama ID (ceo broj, primarni ključ) i SLOVO (karakter).
 * Obezbediti da se vrednost ID automatski generiše ukoliko nije navedena.
 *
 * Zatim izvršavati stavke po redu:
 *  U datu tabelu uneti vrednost SLOVO='a'
 *  U datu tabelu uneti vrednost ID=2, SLOVO='b'
 *  U datu tabelu uneti vrednost SLOVO='c'
 */

-- Pokušaj 1:

CREATE TABLE TEST (
    ID INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    SLOVO CHAR NOT NULL
);

INSERT INTO TEST (SLOVO)
VALUES ('a');

