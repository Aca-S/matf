-- promena u bazi:

SELECT * FROM NOVI_ISPITNI_ROKOVI; -- Gledamo stanje u pogledu - ovde se, očekivano, ne vidi rok 'xxx'.

SELECT * FROM DA.ISPITNIROK; -- Međutim, ako pogledamo baznu tabelu, primetićemo da je novi rok dodat u nju!

-- Iako novi rok ne odgovara definiciji pogleda, ipak je moguće dodati ga u baznu tabelu
-- kroz pogled. Ovo je moguće sprečiti dodavanjem "WITH CHECK OPTION" specifikatora prilikom
