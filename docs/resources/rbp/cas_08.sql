 * 6. Napraviti indeks tabele dosije po imenu i prezimenu u opadajućem redosledu. Zatim ga obrisati.
 */

-- Ukratko o indeksima:
--
-- Zamislimo sledeću situaciju - radimo na platformi za e-trgovinu (npr. ebay, KupujemProdajem, ...)
-- gde je potrebno voditi evidenciju o milionima (verovatno čak i milijardama) različitih oglasa.
-- Podaci o postavljenim oglasima se onda čuvaju u nekoj tabeli "OGLAS", pri čemu je svakom oglasu
-- dodeljen jedinstveni identifikator (koji je ujedno i primarni ključ tabele OGLAS):
--
-- ID       | NAZIV_OGLASA                   | CENA     | ID_KORISNIKA | ...
-- 42       | Proces - Franc Kafka           | 800.00   | 124465       | ...
-- 12345678 | Yamaha F310 - akustična gitara | 21000.00 | 123          | ...
-- 465546   | Lenovo ThinkBook 16 G7         | 79699.00 | 56513        | ...
-- ...
--
-- Prilikom otvaranja stranice oglasa, iz baze je potrebno, na osnovu identifikatora oglasa,
-- dovući sve podatke o njemu - naziv, cenu, opis i ostalo i sve to prikazati posetiocu stranice
-- (suštinski, potrebno je izvršiti upit SELECT * FROM OGLAS WHERE ID = <neki_id>).
--
-- Baza ovo najjednostavnije može izvršiti potpunim skeniranjem tabele OGLAS - prolazi se kroz
-- celu tabelu, red po red, pri čemu se proverava da li je ID trenutnog reda jednak traženom.
-- Međutim, u ovakvom rešenju postoji veliki problem - u tabeli OGLAS imamo milione redova...
-- Potpuno skeniranje jedne takve tabele prilikom otvaranja stranice oglasa je nedopustivo
-- (kako zbog vremena izvršavanja, tako i zbog utrošenih resursa). Potreban nam je način da
-- nađemo odgovarajući red (sa traženim identifikatorom) bez da potencijalno moramo da prođemo
-- kroz celu tabelu. Rešenje za ovo su indeksi!
--
-- Indeksi su posebne strukture, kreirane nad određenim kolonama tabele, koje omogućavaju efikasne
-- upite nad tim kolonama, tj. efikasno pronalaženje redova koji zadovoljavaju neki uslov nad nekom
-- kolonom. Kako oni suštinski rade?
--
-- Zamislimo da uz našu tabelu OGLAS imamo jedan indeks u vidu SORTIRANOG niza identifikatora svih
-- oglasa, pri čemu se uz svaki od tih identifikatora čuva i pokazivač na odgovarajući red u tabeli:
--
-- Indeks nad OGLAS.ID:                 Tabela OGLAS:
--
-- | ID       | Ptr                     | ID       | NAZIV_OGLASA                   | CENA     | ID_KORISNIKA | ...
-- | 1        | ...                     | ...      | ...                            | ...      | ...          | ...
-- | 2        | ...     +-------------> | 42       | Proces - Franc Kafka           | 800.00   | 124465       | ...
-- | ...      | ...     |               | ...      | ...                            | ...      | ...          | ...
-- | 42       | --------+     +-------> | 12345678 | Yamaha F310 - akustična gitara | 21000.00 | 123          | ...
-- | ...      | ...           |         | ...      | ...                            | ...      | ...          | ...
-- | 465546   | --------------|-------> | 465546   | Lenovo ThinkBook 16 G7         | 79699.00 | 56513        | ...
-- | ...      | ...           |         | ...      | ...                            | ...      | ...          | ...
-- | 12345678 | --------------+
-- | ...      | ...
--
-- Pošto su identifikatoru u našem indeksu (nizu) sortirani, to omogućava binarnu pretragu, koja je
-- logaritamske složenosti! Sada, ako želimo da nađemo oglas sa identifikatorom 465546, potrebno je
-- samo da izvršimo binarnu pretragu u okviru indeksa, što je, ako bi imali oko 10,000,000 oglasa
-- samo oko 24 provera. Nakon što u indeksu pronađemo odgovarajući identifikator, dovoljno je ispratiti
-- prateći pokazivač ka odgovarajućem redu u tabeli OGLAS i našli smo traženi red bez skeniranja cele tabele!
--
-- Ovde je prikazan indeks nad jednom kolonom, ali se priča lako uopštava i nad indeksima kreiranim nad
-- više kolona. Ono što je bitno naglasiti je da, iako indeksi mogu da ubrzaju dohvatanje redova, isto
-- tako mogu i usporiti izmene (ubacivanje novih redova, brisanje starih) nad tabelama nad kojim su kreirani
-- jer je sada potrebno održavati i njih. Da bi i ovo bilo efikasno, u praksi indeksi nisu implementirani
-- kao obični nizovi, već kao nešto složenije strukture balansiranih binarnih stabala (B i B+ stabla) ili
-- heš tabela, ali o tome (i o različitim vrstama indeksa) više reči na predavanjima.
--
-- Iako postoji kreiran indeks nad nekim kolonama neke tabele, od optimizatora upita zavisi da li će taj
-- indeks zaista biti i upotrebljen - u nekim situacijama, optimizator može doći do zaključka da je zapravo
-- jeftinije skenirati celu tabelu (naručito ako je ona relativno mala) nego da upotrebi indeks.
--
-- DB2 implicitno kreira indeks nad kolonama koje čine primarni ključ tabele.

-- Rešenje:

CREATE INDEX DOSIJE_IME_DESC_PREZIME_DESC ON DA.DOSIJE (IME DESC, PREZIME DESC);

SELECT *
FROM DA.DOSIJE
WHERE IME = 'Petar' AND PREZIME = 'Knezevic';

DROP INDEX DOSIJE_IME_DESC_PREZIME_DESC;

-- Objašnjenje:
--
-- Kreiranje indeksa se realizuje kroz naredbu
-- CREATE INDEX <naziv indeksa> ON <tabela> (<kolone nad kojima se kreira indeks>);
--
-- Uz kolone se može naglasiti i kako treba sortirati indeks - rastuće ili opadajuće.
-- Ovo u nekim slučajevima može biti korisno za izbegavanje sortiranja prilikom upotrebe
-- ORDER BY u upitu.
--
-- Napomena - ovaj indeks je kreiran nad dve kolone - prvo nad imenom, a onda nad prezimenom.
-- Ovo ne znači da imamo dve odvojene sortirane strukture, već da imamo samo jednu koja je
-- prvo sortirana po imenu studenta, a u okviru istih grupa (više studenata sa istim imenom)
-- je sortirana po prezimenu!

/*
 * 7. Napisati korisnički definisanu funkciju koja vraća cenu predmeta
 * za samofinansirajuće studente. Kao argument se zadaje broj espb.
 * Cena jednog espb boda je 2000 dinara.
 */

-- Rešenje:

CREATE FUNCTION CENA_PREDMETA(ESPB SMALLINT) RETURNS REAL
RETURN ESPB * 2000.0;

SELECT ID, NAZIV,
       CENA_PREDMETA(ESPB) AS CENA
FROM DA.PREDMET;

-- Objašnjenje:
--
-- Prilikom kreiranja funkcije, prvo navodimo njenu deklaraciju - njeno ime,
-- parametre (naziv i tip) i, konačno, tip povratne vrednosti. Nakon deklaracije
-- koristimo RETURN naredbu praćenu izrazom.
--
-- U ovim primerima imaćemo samo najjednostavnije funkcije koje imaju jednu RETURN
-- naredbu. U realnim slučajevima, moguće je kreirati dosta složenije funkcije
-- sa naredbama grananja, petljama i slično.

/*
 * 8. Napisati korisnički definisanu funkciju koja vraća cenu predmeta
 * za samofinansirajuće studente. Kao argument se zadaje id predmeta.
 * Cena jednog espb boda je 2000 dinara.
 */

-- Rešenje:

CREATE FUNCTION CENA_PREDMETA(IDPREDMETA INT) RETURNS REAL
RETURN (SELECT ESPB FROM DA.PREDMET WHERE ID = IDPREDMETA) * 2000.0;

SELECT ID, NAZIV,
       CENA_PREDMETA(ID) AS CENA
FROM DA.PREDMET;

DROP FUNCTION CENA_PREDMETA (SMALLINT);

DROP FUNCTION CENA_PREDMETA (INT);

-- Objašnjenje:
--
-- Princip je isti kao i u prethodnom primeru, samo što sada koristimo podupit da
-- izvučemo ESPB odgovarajućeg predmeta.
--
-- Primetimo da je moguće napraviti više funkcija sa istim imenom, sve dok se tipovi
-- njihovih parametara razlikuju. Prilikom poziva funkcije, izbor koja varijanta će
-- biti pozvana vrši se na osnovu tipa argumenta - tip kolone ESPB je SMALLINT, pa
-- će u prvom slučaju biti pozvana funkcija CENA_PREDMETA(ESPB SMALLINT), dok je tip
-- kolone ID INT, pa će u ovom slučaju biti pozvana funkcija CENA_PREDMETA(IDPREDMETA INT).
-- Ovo se zove "preopterećenje funkcija".
--
-- Brisanje funkcija se vrši sa DROP FUNCTION naredbom. Ukoliko su funkcije preopterećene,
-- potrebno je navesti i tipove parametara one varijante funkcije koju želimo da obrišemo.

/*
 * 9. Napisati korisnički definisnu funkciju koja vraća prosek studenta
 * sa zadatim indekom. Zaokružiti prosek na dve decimale.
 */

-- Rešenje:

CREATE FUNCTION PROSEK(INDEKS_STUDENTA INT) RETURNS REAL
RETURN SELECT DECIMAL(ROUND(AVG(OCENA * 1.0), 2), 4, 2)
       FROM DA.ISPIT
       WHERE INDEKS = INDEKS_STUDENTA AND
             OCENA > 5 AND STATUS = 'o';

SELECT INDEKS, IME, PREZIME,
       PROSEK(INDEKS) AS PROSEK
FROM DA.DOSIJE;

DROP FUNCTION PROSEK;

-- Objašnjenje:
--
-- Ovde prilikom brisanja funkcije nismo morali da navedemo tipove parametara (PROSEK (INT))
-- jer ova funkcija nije preopterećena.

/*
 * 10. Napisati korisnički definisanu funkciju koja vraća koji je danas dan.
 * Vraća naziv dana u nedelju ukoliko je radni dan, inače vraća 'WEEKEND'.
 */

-- Rešenje:

CREATE FUNCTION TELL_ME_THE_DAY() RETURNS VARCHAR(10)
RETURN CASE
           WHEN DAYOFWEEK(CURRENT_DATE) IN (7, 1) THEN 'WEEKEND'
           ELSE DAYNAME(CURRENT_DATE)
       END;

VALUES TELL_ME_THE_DAY();

DROP FUNCTION TELL_ME_THE_DAY;

