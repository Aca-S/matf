-- Objašnjenje:
--
-- Želimo da se ID generiše automatski ukoliko ga navedemo. Ako se setimo
-- prvog zadatka sa prethodnog časa, automatsko generisanje vrednosti smo
-- postigli dodavanjem "GENERATED ALWAYS AS IDENTITY", pa bi verovatno bilo
-- za očekivati da će isto rešenje raditi i ovde.
--
-- Prva INSERT naredba se izvršava ispravno. Međutim, već u drugoj dobijamo
-- grešku. Šta se desilo? "GENERATED ALWAYS AS IDENTITY" znači da će se vrednost
-- UVEK generisati automatski, tj. time se lišavamo mogućnosti da je mi
-- eksplicitno navedemo. Ovo rešavamo upotrebom "GENERATED BY DEFAULT AS IDENTITY".

-- Pokušaj 2:

CREATE TABLE TEST (
    ID INT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    SLOVO CHAR NOT NULL
);

INSERT INTO TEST (SLOVO)
VALUES ('a');

INSERT INTO TEST
VALUES (2, 'b');

SELECT * FROM TEST;

INSERT INTO TEST (SLOVO)
VALUES ('c');

DROP TABLE TEST;

-- Objašnjenje:
--
-- Ovde smo, u odnosu na prethodni pokušaj, zamenili "ALWAYS" sa "BY DEFAULT".
-- ID će sada biti generisan automatski, ali samo ukoliko ga mi eksplicitno
-- ne navedemo, što je upravo ono što želimo.
--
-- Prva dva INSERT-a prolaze, međutim, sada imamo problem sa trećim. Šta se sada desilo?
-- "GENERATED ... AS IDENTITY" zapravo kreira generator brojeva u pozadini. Taj generator
-- ima svoju početnu vrednost, korak za koji se uvećava i još dodatnih osobina (minimalna,
-- maksimalna vrednost, ciklično generisanje i slično). Svaki put kada se automatski
-- generiše novi broj pomoću generatora, taj broj se pamti kao poslednji generisani. Naredni
-- broj se računa u odnosu na njega.
--
-- Međutim, ovo se dešava samo kada se brojevi zaista automatski generišu - ne i kada
-- mi eksplicitno navedemo neki ID! Ovde se onda desilo sledeće:
--
-- 1) Prvi INSERT - automatski generišemo ID=1. Ovo se pamti kao stanje generatora.
-- 2) Drugi INSERT - mi smo naveli ID=2. Generator nije generisao ništa, te je njegovo stanje ostalo nepromenjeno.
-- 3) Treći INSERT - ponovo tražimo automatsko generisanje. Međutim, generator računa sledeći ID
--                   u odnosu na prethodni generisani ID, tj. sada generiše 2. Međutim, mi već imamo ID=2
--                   u tabeli - narušili smo ograničenje primarnog ključa!

-- Rešenje:

CREATE TABLE TEST (
    ID INT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY (START WITH 100 INCREMENT BY 2),
    SLOVO CHAR NOT NULL
);

INSERT INTO TEST (SLOVO)
VALUES ('a');

INSERT INTO TEST
VALUES (2, 'b');

INSERT INTO TEST (SLOVO)
VALUES ('c');

SELECT * FROM TEST;

DROP TABLE TEST;

