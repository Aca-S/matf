-- Kako da zaobiđemo kolizije u ID-jevima koji su automatski generisani i onim
-- koje smo mi ručno odredili? Jedna opcija je da promenimo opseg automatski generisanih
-- identifikatora. To možemo uraditi sa dodatnim "(START WITH 100 INCREMENT BY 2)".
-- Identifikatori će se sada generisati od broja 100, sa uvećanjem od 2. Nakon svih
-- INSERT naredbi, u tabeli imamo redove (ID=100, SLOVO='a'), (ID=2, SLOVO='b'), (ID=102, SLOVO='c').

